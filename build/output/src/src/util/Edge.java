package util;

public class Edge {
	private Node src, dstn;
	private Object data;

	Edge(Node src, Node dstn) {
		this.src = src;
		this.dstn = dstn;
	}
	
	public void setData(Object data) {
		this.data = data;
	}
	
	/**
	 * @return the data
	 */
	public Object getData() {
		return data;
	}
	
	/**
	 * @return the dstn
	 */
	public Node getDstn() {
		return dstn;
	}

	/**
	 * @return the src
	 */
	public Node getSrc() {
		return src;
	}

}
